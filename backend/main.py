from seed_data import DEFAULT_CHARACTERS

load_dotenv()

app = FastAPI(title="MyCharacterAI API")
from fastapi.middleware.cors import CORSMiddleware
app.add_middleware(
       CORSMiddleware,
       allow_origins=["*"], # we will lock this later. * works for first deploy
       allow_credentials=True,
       allow_methods=["*"],
       allow_headers=["*"],
   )

